package org.example;



public class Coure {

  private long cno;
  private String cname;
  private long cpno;
  private long ccredit;

  public long getCno() {
    return cno;
  }

  public void setCno(long cno) {
    this.cno = cno;
  }

  public String getCname() {
    return cname;
  }

  public void setCname(String cname) {
    this.cname = cname;
  }

  public long getCpno() {
    return cpno;
  }

  public void setCpno(long cpno) {
    this.cpno = cpno;
  }

  public long getCcredit() {
    return ccredit;
  }

  public void setCcredit(long ccredit) {
    this.ccredit = ccredit;
  }
}
