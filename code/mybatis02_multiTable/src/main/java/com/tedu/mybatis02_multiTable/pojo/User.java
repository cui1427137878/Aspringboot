package com.tedu.mybatis02_multiTable.pojo;
//对应user表
public class User {
//user_id列
	Integer userId;
	//username列
	String username;
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	
	
}
